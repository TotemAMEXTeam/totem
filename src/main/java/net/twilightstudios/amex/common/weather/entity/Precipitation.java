package net.twilightstudios.amex.common.weather.entity;

public class Precipitation {

	private double rain;
	private double snow;
	
	public double getRain() {
		return rain;
	}
	public void setRain(double rain) {
		this.rain = rain;
	}
	public double getSnow() {
		return snow;
	}
	public void setSnow(double snow) {
		this.snow = snow;
	}
}